// floor-цены (name -> TON), заполняются из nft_prices.json
window.NFT_PRICES = {};

// Тир и «монетная» стоимость по флору в TON
function nftTier(ton){
  if(ton>=20) return 'legendary';
  if(ton>=5)  return 'epic';
  if(ton>=1)  return 'rare';
  return 'common';
}
const NFT_TON_TO_COINS = 200; // 1 TON ≈ 200 монет (для игровой экономики)

function applyNftPrices(prices){
  window.NFT_PRICES = prices || {};
  NFT_GIFTS.forEach(function(g){
    var p = window.NFT_PRICES[g.name];
    var ton = (p && p.price_ton) ? Number(p.price_ton) : 0;
    g.priceTon = ton;
    g.tier  = nftTier(ton);
    g.value = Math.max(1, Math.round(ton * NFT_TON_TO_COINS));
    g.minValue = g.value; g.maxValue = g.value;
    g.weight = ton>=20?1 : ton>=5?3 : ton>=1?8 : 15;
    g.emoji = '🎁';
  });
}

// Регистрируем NFT в системах подарков/кейсов и в картинках
function registerNftGifts(){
  try{
    NFT_GIFTS.forEach(function(g){
      if(typeof GIFT_IMGS!=='undefined') GIFT_IMGS[g.type]=g.img;
    });
    if(typeof CASE_GIFTS!=='undefined')
      NFT_GIFTS.forEach(function(g){ if(!CASE_GIFTS.some(x=>x.type===g.type)) CASE_GIFTS.push(g); });
    if(typeof GIFT_SYSTEM!=='undefined' && GIFT_SYSTEM.gifts)
      NFT_GIFTS.forEach(function(g){ if(!GIFT_SYSTEM.gifts.some(x=>x.type===g.type)) GIFT_SYSTEM.gifts.push(g); });
  }catch(e){ console.error('registerNftGifts', e); }
}

// Загрузка цен + регистрация
(function initNft(){
  applyNftPrices({});          // дефолт (0 TON) пока не загрузилось
  registerNftGifts();
  fetch('nft_prices.json?_='+Date.now())
    .then(r=>r.ok?r.json():{})
    .then(function(data){
      applyNftPrices((data && data.prices) ? data.prices : data);
      if(typeof renderCaseCards==='function') try{renderCaseCards();}catch(e){}
    })
    .catch(function(){});
})();
